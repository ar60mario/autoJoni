    private void cargarFrame() {
        codigoTxt.setText(articulo.getProducto().getCodigo().toString());
        productoTxt.setText(articulo.getProducto().getDetalle());
        gravadoTxt.setText(df.format(articulo.getGravado()));
        impuestoTxt.setText(df.format(articulo.getImpuesto()));
        ivaTxt.setText(df.format(articulo.getIva()));
        totalTxt.setText(df.format(articulo.getTotal()));
        if (articulo.getActivo()) {
            activoChk.setSelected(true);
        } else {
            activoChk.setSelected(false);
        }
        if (articulo.getPorcentual() != null) {
            porcentualTxt.setText(dfp.format(articulo.getPorcentual()));
        } else {
            porcentualTxt.setText(dfp.format(0));
        }
    }
