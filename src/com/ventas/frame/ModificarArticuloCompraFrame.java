    private void grabar() {
        if (validar()) {
//            ArticuloCompra ac = new ArticuloCompra();
//            ac.setProducto(producto);
            Double gravado = Double.valueOf(gravadoTxt.getText().replace(",", "."));
            Double impuesto = Double.valueOf(impuestoTxt.getText().replace(",", "."));
            Double iva = Double.valueOf(ivaTxt.getText().replace(",", "."));
            Double total = Double.valueOf(totalTxt.getText().replace(",", "."));
            Float porcentual = Float.valueOf(porcentualTxt.getText().replace(",", "."));
            articulo.setGravado(gravado);
            articulo.setImpuesto(impuesto);
            articulo.setIva(iva);
            articulo.setTotal(total);
            articulo.setPorcentual(porcentual);
            if (activoChk.isSelected()) {
                articulo.setActivo(true);
            } else {
                articulo.setActivo(false);
            }
            try {
                new ArticuloCompraService().updateArticuloCompra(articulo);
                volver();
            } catch (Exception ex) {
                Logger.getLogger(ModificarArticuloCompraFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    private void volver() {
        AbmArticulosPorMontoFrame aapmf = new AbmArticulosPorMontoFrame();
        aapmf.setVisible(true);
        this.dispose();
    }

